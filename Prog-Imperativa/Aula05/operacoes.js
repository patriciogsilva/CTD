// adição

console.log(2+2);

// subtração

console.log(10-5);

// multiplicação

console.log(10*5);

// divisão

console.log(10 / 2);

// incremento

let numero = 10;
console.log( ++ numero);

// decremento

console.log( -- numero);

// comparação de maior

console.log(10 > 5);

// comparação de menor

console.log(10 < 5);  

// comparação de menor ou igual

console.log(3 <= 2);

// comparação de maior ou igual

console.log(20 >= 14);

// comparação de igualdade

console.log(10 == 10);
console.log(10 == 20);
console.log("ola" == "ola");

// comparação de igualdade rigido

console.log(5 === "5");
// se colocarmos (==) ele compara os valores e se colarmos (===) ele compra os valores e o tipo

// compração de diferenca

console.log(10 != 10);
console.log(10 != 20);
console.log("ola" != "oi");
// string devem ser colocadas entre aspas

// cmparação de diferente rigido

console.log(10 !== "10");
// nesse caso os valores são iguais mas o tipo é diferente

// OPERADORES LOGICOS

// comparador logico && (END) - usado para juntar condições

console.log(10 > 5 && 8 < 10);
console.log(10 > 5 && 8 > 10 && 11 > 5);

// comparador logico OR || (ou)

console.log(10 > 5 || 8 > 10);

// comparador de negação !

console.log(!5 > 3);


// operador de concatenação
let nome = "Jão";
let sobrenome = "Oliveira";

console.log(nome + " " + sobrenome)
// utilize dessa formara para unir duas ou mais palavras ou frases utilizando (" ") para separar